public class Numero {

  private float valor;

   // contrutor 
  public Numero(){
    this.valor = 0.0f;
  }

  // metodos para setar o valor 
  public void setValor(float valor) {
    this.valor = valor;
  }
// metodos para recuperar o valor
  public float getValor() {
    return this.valor;
  }

  

  
}
